package cursojava.aula10;

public class Variaveis {

	
	public static void main(String[] args) {
	
		String mome = "Bruno";
		int idade = 18;
		double altura = 1.80;
		float peso = 81;
		String cidade = "Rio de Janeiro";
		String estado = "RJ";
		
		System.out.println("Seu nome: " + mome);
		System.out.println("Sua idade: " + idade);
		System.out.println("Sua altura: " + altura + "m");
		System.out.println("Seu peso: " + peso + "Kg");
		System.out.println("Sua cidade: " + cidade);
		System.out.println("Seu estado: " + estado);

	}

}
